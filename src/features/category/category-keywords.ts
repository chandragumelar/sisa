import { FALLBACK_CATEGORY } from './category.types'

export function normalizeLabel(raw: string): string {
  return raw
    .toLowerCase()
    .replace(/[^a-z0-9\s]/g, ' ')
    .replace(/\s+/g, ' ')
    .trim()
}

// Single-word keywords: matched as whole word (must appear as a standalone token).
// Multi-word keywords: matched as substring in the normalized label.
function matchesKeyword(normalized: string, words: string[], keyword: string): boolean {
  if (keyword.includes(' ')) {
    return normalized.includes(keyword)
  }
  return words.includes(keyword)
}

