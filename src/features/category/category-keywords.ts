function matchCategory(label: string, dict: Record<string, string[]>): string {
  const normalized = normalizeLabel(label)
  const words = normalized.split(' ')

  for (const [category, keywords] of Object.entries(dict)) {
    for (const keyword of keywords) {
      if (matchesKeyword(normalized, words, keyword)) {
        return category
      }
    }
  }
  return FALLBACK_CATEGORY
}

export function suggestExpenseCategory(label: string): string {
  if (!label.trim()) return FALLBACK_CATEGORY
  return matchCategory(label, EXPENSE_KEYWORDS)
}

export function suggestIncomeCategory(label: string): string {
  if (!label.trim()) return FALLBACK_CATEGORY
  return matchCategory(label, INCOME_KEYWORDS)
}
